DATA_691 as (
select
     to_date(AP_MVT_TIME_UTC) as DATE_FLT
    ,SRC_AIRPORT APT_ICAO
    ,SUM (CASE WHEN SRC_Phase IN ('DEP') AND AP_C_FLTRUL IN ('IFR') THEN NVL (1, 0) ELSE 0 END) as FLT_DEP_IFR_2
    ,SUM (CASE WHEN SRC_Phase IN ('ARR') AND AP_C_FLTRUL IN ('IFR') THEN NVL (1, 0) ELSE 0 END) as FLT_ARR_IFR_2
    ,SUM (CASE WHEN SRC_Phase IN ('ARR') AND AP_C_FLTRUL IN ('IFR') THEN NVL (1, 0) ELSE 0 END) + SUM (CASE WHEN SRC_Phase IN ('ARR') AND AP_C_FLTRUL IN ('IFR') THEN NVL (1, 0) ELSE 0 END)  FLT_TOT_IFR_2
    ,SUM (CASE WHEN SRC_Phase IN ('DEP') AND AP_C_FLTRUL IN ('VFR') THEN NVL (1, 0) ELSE 0 END) as FLT_DEP_VFR_2
    ,SUM (CASE WHEN SRC_Phase IN ('ARR') AND AP_C_FLTRUL IN ('VFR') THEN NVL (1, 0) ELSE 0 END) as FLT_ARR_VFR_2
    ,SUM (CASE WHEN SRC_Phase IN ('ARR') AND AP_C_FLTRUL IN ('VFR') THEN NVL (1, 0) ELSE 0 END) + SUM (CASE WHEN SRC_Phase IN ('ARR') AND AP_C_FLTRUL IN ('IFR') THEN NVL (1, 0) ELSE 0 END)  FLT_TOT_VFR_2
from SWH_FCT.FAC_APDS_FLIGHT_IR691
      WHERE ((adep_icao  IN (SELECT apt_id FROM APT_SELECTION_2014) and AP_MVT_TIME_UTC >= '01 JAN 2014' AND AP_MVT_TIME_UTC <'01 JAN 2015')
      OR (adep_icao  IN (SELECT apt_id FROM APT_SELECTION_2015) and AP_MVT_TIME_UTC >= '01 JAN 2015' AND AP_MVT_TIME_UTC <'01 JUN 2015'))

 --   AND ADEP_ICAO <> ADES_ICAO -- remove circular flights
    group by to_date(AP_MVT_TIME_UTC) ,SRC_AIRPORT
   ),

DATA_NM as (select
p.FLT_DATE AS DATE_FLT,
p.ID,
u.code as APT_ICAO,
u.NAME,
NVL (p.ttf_dep, 0) as FLT_DEP_1,
NVL (p.ttf_arr, 0) as FLT_ARR_1,
NVL (p.ttf_arr, 0) + NVL (p.ttf_dep, 0) as FLT_TOT_1
     FROM prudev.pru_fact_traffic_airspace p,
          prudev.pru_airport u
    WHERE p.ID = u.ID AND p.TYPE = 'AIRPORT'
          and FLT_DATE >= '01 JAN 2014' AND FLT_DATE <'01 JUL 2015'
          and u.code IN (SELECT apt_id FROM APT_SELECTION)
          )

Select
     to_char(n.DATE_FLT, 'YYYY') YEAR
   , to_char(n.DATE_FLT, 'MM') MONTH_NUM
   , to_char(n.DATE_FLT, 'MON') MONTH_MON
   , n.DATE_FLT FLT_DATE
   , n.APT_ICAO APT_ICAO
   , n.NAME APT_NAME
   , 'XXXX' STATE_NAME
   , FLT_DEP_1
   , FLT_ARR_1
   , FLT_TOT_1
   , FLT_DEP_IFR_2
   , FLT_ARR_IFR_2
   , FLT_TOT_IFR_2
from DATA_691 a, DATA_NM n

WHERE n.APT_ICAO = a.APT_ICAO(+) AND n.DATE_FLT = a.DATE_FLT(+)
ORDER by 4,5
